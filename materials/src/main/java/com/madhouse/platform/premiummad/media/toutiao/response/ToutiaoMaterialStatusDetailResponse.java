package com.madhouse.platform.premiummad.media.toutiao.response;

public class ToutiaoMaterialStatusDetailResponse {
	private String status;
	private String adid;
	private String reason;
	private String width;
	private String height;
	private String img_url;
	private String click_through_url;
	private String error;

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getAdid() {
		return adid;
	}

	public void setAdid(String adid) {
		this.adid = adid;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getWidth() {
		return width;
	}

	public void setWidth(String width) {
		this.width = width;
	}

	public String getHeight() {
		return height;
	}

	public void setHeight(String height) {
		this.height = height;
	}

	public String getImg_url() {
		return img_url;
	}

	public void setImg_url(String img_url) {
		this.img_url = img_url;
	}

	public String getClick_through_url() {
		return click_through_url;
	}

	public void setClick_through_url(String click_through_url) {
		this.click_through_url = click_through_url;
	}

	public String getError() {
		return error;
	}

	public void setError(String error) {
		this.error = error;
	}

}
